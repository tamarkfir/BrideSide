/* ---------- תקציר מצטבר של כל תשובות הזוג (משמש את Phase 2) ---------- */

export function coupleSummary(state: SessionState): string {
  const { a, b } = state.brides;
  const lines: string[] = [`הכלות: ${a.name} ו${b.name}.`];

  QUESTIONNAIRE.forEach((qPage, i) => {
    const page = state.pages[i];
    if (!page) return;

    for (const field of qPage.fields) {
      const ans = getAnswer(page, field.id);

      if (field.kind === "traditions") {
        for (const topic of TRADITIONS) {
          const t = getAnswer(page, `${field.id}__${topic}`);
          if (t.choice) {
            const label = t.choice === "in" ? "כן" : t.choice === "maybe" ? "אולי" : "לא";
            lines.push(`מנהג "${topic}": ${label}${t.note ? ` — ${t.note}` : ""}`);
          }
        }
        continue;
      }

      const label = field.label
        .replace(/{partner}/g, "בת הזוג")
        .replace(/{name}/g, "")
        .replace(/^[\s,]+/, "")
        .trim();
      if (field.kind === "perBride" && ans.bride) {
        if (ans.bride.a) lines.push(`${a.name} — ${label}: ${ans.bride.a}`);
        if (ans.bride.b) lines.push(`${b.name} — ${label}: ${ans.bride.b}`);
      } else if (ans.list?.length) {
        lines.push(`${label}: ${ans.list.join(", ")}`);
      } else if (ans.choice || ans.yesno) {
        const val = ans.choice ?? (ans.yesno === "yes" ? "כן" : ans.yesno === "no" ? "לא" : "");
        if (val) lines.push(`${label}: ${val}${ans.note ? ` (${ans.note})` : ""}`);
      } else if (ans.text) {
        lines.push(`${label}: ${ans.text}`);
      }
    }

    for (const photo of page.photos) {
      if (photo.caption) lines.push(`כיתוב תמונה ב"${qPage.title}": ${photo.caption}`);
    }
  });

  return lines.join("\n");
}

/* ---------- Phase 1.5: מחקר שירים — מציאת מילים אמיתיות והצעת שירים מתאימים ---------- */

export type VerifiedSong = {
  name: string;
  artist: string;
  /** שורות אמיתיות מהשיר — null אם לא נמצאו / לא אומתו בוודאות */
  excerpt: string | null;
  /** ה-URL שממנו הועתקו המילים (חובה כש-excerpt לא null) — מנגנון מחויבות נגד המצאה */
  source?: string | null;
  /** הסבר למה השיר מתאים לזוג (רלוונטי להצעות חדשות) */
  why?: string;
  /** האם זו הצעה של ה-AI (לא שיר שהזוג בחרו) */
  suggested?: boolean;
};

/**
 * בונה בקשה ל-AI למחקר שירים עם חיפוש רשת.
 * קריאה זו מתבצעת ב-plain text (לא JSON mode) עם googleSearch מופעל.
 */
export function buildSongResearchPrompt(state: SessionState): string {
  const { a, b } = state.brides;
  const summary = coupleSummary(state);
  return `לפנייך פרופיל של הזוג ${a.name} ו${b.name} שבונות טקס חתונה:

${summary}

המשימה שלך היא שתיים:

**1. מחקר שירים שהזוג בחרו:**
**תפקידך הוא למצוא את המילים — לא לוותר.** עבור כל שיר שמוזכר בפרופיל, חפשי ברשת באמת (נסי כמה אתרי מילים: tab4u, shironet, genius וכו') ומצאי 2–4 שורות אהובות מהשיר. השקיעי מאמץ — רוב השירים נמצאים ברשת.

**איך לצטט נכון (כדי שהציטוט יהיה אמיתי ולא של שיר אחר):**
- העתיקי את השורות **מילה-במילה** מעמוד מילים שמצאת בחיפוש, ומלאי את שדה "source" עם ה-URL של אותו עמוד. ציטוט תמיד בא ממקור שראית — לא מהזיכרון.
- ודאי שזה השיר הנכון: שם השיר **והאמן** תואמים למה שביקשו. שמות שירים בעברית חוזרים (למשל "ביחד", "יחד") — אם מצאת כמה שירים באותו שם, בחרי את זה של האמן הספציפי שביקשו.
- אל תנסחי מחדש, אל תשלימי מהזיכרון, ואל "תשחזרי" שורות.

excerpt: null הוא **מוצא אחרון בלבד** — רק אם באמת חיפשת ולא מצאת שום עמוד מילים אמין לשיר הזה של האמן הזה. אל תבחרי null מתוך זהירות יתר. **אסור להמציא מילים, אבל גם אסור לוותר בלי לחפש.**

**2. הצעת שירים נוספים (חלופות מאומתות):**
חשוב לדעת: **הצעה שלך (suggested: true) שלא תעבור אימות — לא תיכלל בטקס כלל.** לכן הצעות שווֹת רק אם תוכלי לאמת את מילותיהן. כללים:
- הציעי שירים **מתאימים לחתונה ולטקס** — שירי אהבה, זוגיות, חיבור, התחייבות, שמחה. **לא** שירים עצובים, שירי פרידה, שירי מחאה או שירים שלא שייכים לרגע של חופה.
- הציעי שירים שמתאימים **במיוחד לזוג הזה** — לפי הרקע שלהן, הערכים והאווירה שעלו בפרופיל — ולא שירים גנריים.
- אם שיר שהזוג בחרו לא עבר אימות — מצאי שיר חלופי קרוב ברוחו שכן **תוכלי לאמת את מילותיו בוודאות**.
- דאגי שיהיו לפחות 2–3 שירים עם excerpt מאומת (לא null). אם הצעה אחת לא מאומתת — הוסיפי אחרת מאומתת במקומה.
- גם להצעות — חפשי ומצאי מילים אמיתיות (העתקה מילה-במילה מ-source). הסבירי למה כל שיר מתאים לזוג הזה דווקא.

**החזירי JSON בפורמט הזה (רק ה-JSON, בלי טקסט נוסף):**
{"songs": [
  {"name": "שם השיר", "artist": "שם האמן", "excerpt": "שורות שהועתקו מילה-במילה או null", "source": "ה-URL שממנו הועתקו המילים או null", "why": "למה מתאים לזוג הזה", "suggested": false},
  {"name": "שיר שאני מציעה", "artist": "אמן", "excerpt": "שורות שהועתקו מילה-במילה או null", "source": "URL או null", "why": "הסבר מפורט ואישי", "suggested": true}
]}

חשוב: כל excerpt חייב להיות מילות שיר שהעתקת מילה-במילה מ-source אמיתי של אותו אמן בדיוק. בכל ספק — null.`;
}

/* ---------- Phase 2: עיבוד התשובות לספרון טקס ---------- */

/**
 * בונה את הבקשה ל-AI לייצר את טיוטת ספרון הטקס מתוך כל תשובות הזוג.
 * מצפה ל-JSON במבנה CeremonyBooklet (lib/types.ts).
 */
export function buildCeremonyPrompt(state: SessionState, verifiedSongs: VerifiedSong[] = []): string {
  const { a, b } = state.brides;

  // כל שיר ברשימה ייכלל בטקס (גם הצעות, גם בחירות הזוג). שיר עם "קטע אמיתי" —
  // מצטטים מילה-במילה. שיר בלי — מזכירים בשם ובמשמעות בלבד, ללא ציטוט שורות.
  const songsSection = verifiedSongs.length > 0
    ? `\n\nשירים לטקס (כללי את כולם — אל תשמיטי אף אחד):\n${verifiedSongs.map(s => {
        const tag = s.suggested ? "[הצעה שלך לזוג]" : "[שיר שהזוג בחרו]";
        const excerpt = s.excerpt
          ? `\n  קטע אמיתי (צטטי מילה-במילה): "${s.excerpt}"`
          : "\n  (אין מילים מאומתות — הזכירי בשם ובמשמעות בלבד, בלי לצטט שורות)";
        const why = s.why ? `\n  מדוע מתאים: ${s.why}` : "";
        return `- ${tag} ${s.name} — ${s.artist}${excerpt}${why}`;
      }).join("\n")}`
    : "";

  return `לפנייך כל מה שהזוג ${a.name} ו${b.name} שיתפו בתהליך. בני מתוכו טיוטה של ספרון טקס חתונה אישי, שהן יוכלו לערוך.

מה שהן שיתפו:
${coupleSummary(state)}${songsSection}

הנחיות לכתיבה:
- כתבי טקס שלם, אישי ופואטי — אבל פשוט וברור, לא מליצי ולא מוגזם.
- שירים ומילים: **שלבי בטקס את כל השירים שמופיעים בסעיף "שירים לטקס" למעלה — גם את ההצעות שלך וגם את בחירות הזוג. אל תשמיטי אף שיר.** כל שיר ראוי לאזכור משלו (איפה בטקס הוא משתלב ומה הוא מביא לזוג). **אסור להזכיר שום שיר שלא ברשימה.** ציטוט מילות שיר מותר **רק** מתוך "קטע אמיתי" שמופיע שם — מילה-במילה ללא שינוי. לשיר שמסומן "(אין מילים מאומתות)" — **אסור לצטט ממנו שורות**, אבל עדיין הזכירי אותו בשמו ובמשמעותו לזוג. לשורות שהזוג הדביקו בשדה "שורות ספציפיות" — צטטי במדויק. אסור בהחלט להמציא, לנחש, או "לשחזר" שורות או שירים — גם אם נדמה לך שאת זוכרת אותם.
- פתחי בקטע "דמיון מודרך" קצר (kind: imagery) שמזמין אותן ואת הקהל להיכנס לרגע.
- בני את מהלך הטקס (kind: flow) לפי הבחירות שלהן בלבד — כניסה, חופה, מי מנחה, מי עומד איתן, המנהגים שבחרו, הברכות שביקשו.
- אם בחרו נוסח יהודי מסורתי או שילוב — כתבי ברכות בנוסח מסורתי (kind: blessing), אך **הקפידי על הכלל הבא:** הפנייה לאלוהים חייבת להישאר בנוסח המסורתי המקורי ("ברוך אתה ה' אלוהינו מלך העולם", "בורא", "יוצר"). שוני ללשון נקבה **רק** את ההתייחסות לזוג הכלות (במקום חתן וכלה - כלה וכלה, במקום רעים אהובים - רעות אהובות). אסור להפוך את אלוהים לנקבה. המילה "חתן" אסורה לחלוטין — היא לא תופיע באף מקום בטקס.
- **שבע ברכות:** אם בחרת להציג את שבע הברכות המסורתיות, **קבצי את כל 7 הברכות לתוך מקטע (section) אחד משותף** תחת הכותרת "שבע ברכות". אל תפצלי אותן ל-7 מקטעים/עמודים נפרדים. פצלי ברכות לעמודים שונים רק אם אלו ברכות ארוכות ואישיות שנכתבו על ידי המשפחה או החברות.
- שלבי את הנדרים האישיים שכתבו (kind: vow), כל אחת לשנייה, בשם הכלה (attribution = שם הכלה).
- ציטוטים מאומתים מתוך שירים יופיעו כ-kind: quote או reading, עם attribution למקור (שם השיר ואמן). שיר ללא מילים מאומתות יופיע כ-kind: flow — אזכור בשם ובמשמעות, בלי ציטוט שורות. כל שיר ברשימה צריך להופיע בטקס באחת מהדרכים האלה.
- סיימי בקטע סיום חם (kind: closing).
- כלל ברזל: אל תניחי יחסים חמים עם המשפחה אלא אם נכתב במפורש. אם לא נכתב על משפחה — אל תזכירי אותה.
- אל תמציאי עובדות, שמות או רגעים שלא נמסרו.
- לשון נקבה לכל אורך הטקס. עברית תקנית ומדויקת — בלי שגיאות כתיב, דקדוק או פיסוק. קראי כל משפט שוב לפני שאת מסיימת.

החזירי JSON תקין בלבד, בלי טקסט עוטף ובלי גדרות markdown, במבנה:
{
  "title": "הטקס של ${a.name} ו${b.name}",
  "dedication": "משפט הקדשה קצר ואופציונלי",
  "sections": [
    { "kind": "imagery", "title": "כותרת הקטע", "body": "תוכן הקטע", "attribution": "מקור אם רלוונטי" }
  ]
}`;
}

/**
 * מעבר הגהה שני על הספרון — מתקן עברית בלי לשנות תוכן.
 * מקבל את ה-JSON שנוצר ומחזיר אותו מתוקן באותו מבנה.
 */
export function proofreadCeremonyPrompt(booklet: unknown): string {
  return `לפנייך טיוטת ספרון טקס בפורמט JSON. תפקידך להגיה אותה — לתקן את העברית בלבד.

מה לעשות:
- תקני שגיאות כתיב, דקדוק ופיסוק.
- ודאי לשון נקבה עקבית לכל אורך הטקסט (שתי כלות, בלי לשון זכר).
- חפשי במיוחד את המילה "חתן" ואת נוסחי שבע הברכות ("קול חתן וקול כלה", "מצהלות חתנים") — והמירי אותם ללשון נקבה לשתי כלות ("קול כלה וקול כלה", "מצהלות כלות מחופתן"). המילה "חתן" אסורה ולא תופיע בפלט.
- שמרי על משפטים זורמים וטבעיים.

מה אסור:
- אל תשני את המשמעות, את הסדר או את מספר הקטעים.
- אל תוסיפי, אל תמחקי ואל תקצרי תוכן.
- שמרי בדיוק על אותם מפתחות (title, dedication, sections, kind, body, attribution).

ה-JSON להגהה:
${JSON.stringify(booklet)}

החזירי JSON תקין בלבד, באותו מבנה, בלי טקסט עוטף ובלי גדרות markdown.`;
}

