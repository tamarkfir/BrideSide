/* ============================================================================
 *  BrideSide — השאלון (מקור יחיד לכל ניסוחי השאלות)
 * ============================================================================
 *
 *  כל נוסח של שאלה נמצא כאן ורק כאן. כדי לשנות ניסוח — ערכי את הטקסט בשדות
 *  label / subLabel / placeholder / suggestions. אין כאן לוגיקה, רק מילים.
 *  השינוי ייכנס לתוקף ברענון הדף.
 *
 *  שפה: ברוח התקשורת המקרבת (לינדה נתן כהן) והרבניות הרפורמיות —
 *    • מזמינות, לא מצוות ("ספרו לי", "אם בא לכן", ולא "כתבו")
 *    • שואלות על רגש וצורך, לא על עמדה
 *    • בלי שיפוט, בלי הנחות (לא מניחות קרבה משפחתית או דתיות)
 *    • לשון נקבה, כולל שתי כלות
 *
 *  אסימונים שמוחלפים אוטומטית בזמן הצגה:
 *    {partner} — בשדה perBride: שם הכלה השנייה (זו שעליה כותבים)
 *    {names}   — "נועה ותמר"
 * ========================================================================== */

export type QFieldKind =
  | "text" // שורה אחת
  | "longtext" // טקסט חופשי
  | "perBride" // תיבה נפרדת לכל כלה (כתיבה לחוד)
  | "chips" // בחירת תגיות + הוספה משלהן
  | "songs" // עד 3 שירים (תומך בחיפוש ספוטיפיי)
  | "spotify" // שיר בודד (תומך בחיפוש ספוטיפיי)
  | "yesno" // כן / לא (+ הערה)
  | "choice" // בחירה אחת מתוך אפשרויות (+ "אחר")
  | "traditions"; // רשימת מנהגים, לכל אחד כן/לא/אולי + איך לשלב

export type QField = {
  id: string;
  kind: QFieldKind;
  /** נוסח השאלה — מותר להשתמש ב-{partner} ו-{names} */
  label: string;
  /** שורת הסבר קטנה מתחת לשאלה */
  subLabel?: string;
  placeholder?: string;
  /** הצ'יפים — הצעות מהירות ללחיצה (קבועות) */
  suggestions?: string[];
  /** לאפשרויות בחירה (choice) */
  options?: string[];
  /** האם להוסיף תיבת טקסט חופשי אחרי כן/לא או בחירה */
  note?: boolean;
};

export type QPage = {
  key: string;
  /** כותרת העמוד */
  title: string;
  /** שם הקבוצה/הפרק (מוצג קטן מעל הכותרת) */
  group: string;
  /** משפט פתיחה חם לעמוד (אופציונלי) */
  intro?: string;
  fields: QField[];
  /** האם מותר להוסיף תמונת המחשה בעמוד הזה */
  allowPhoto?: boolean;
};

